package Modules;

public class Pessoa {
    private int id;
    private String nome;
    private String telefone;
    private String CPF;
    Pessoa(){

    }
    Pessoa(int id, String nome, String telefone, String CPF){
        this.id = id;
        this.CPF = CPF;
        this.nome = nome;
        this.telefone = telefone;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }
}
